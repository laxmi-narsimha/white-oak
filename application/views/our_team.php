    <script>
        function mobileOnlySlider() {
            $('.ourPartnerSwiper').slick({
                centerMode: true,
                slidesToShow: 3,
                focusOnSelect: true,
                dots: false,
                infinite: false,
                responsive: [
                    {
                        breakpoint: 768,
                        settings: {
                            dots: false,
                            arrows: false,
                            slidesToShow: 2.5,
                            infinite: false,
                            centerMode: false,
                        }
                    }
                ]
            });
        }
        if (window.innerWidth < 768) {
            mobileOnlySlider();
        }

        $(window).resize(function (e) {
            if (window.innerWidth < 768) {
                if (!$('.ourPartnerSwiper').hasClass('slick-initialized')) {
                    mobileOnlySlider();
                }

            } else {
                if ($('.ourPartnerSwiper').hasClass('slick-initialized')) {
                    $('.ourPartnerSwiper').slick('unslick');
                }
            }
        }); 
    </script>

